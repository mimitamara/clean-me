    /**
     * Handle the Task "created" event.
     *
     * @param  \App\Models\Task  $task
     * @return void
     */
    public function created(Task $task)
    {
        $steps = Step::where('category_id', $task->category_id)->get();

        foreach ($steps as $step) {
            TaskStep::create([
                'task_id' => $task->id,
                'step_id' => $step->id,
                'status' => 'waiting',
            ]);
        }
    }

